public class DataTypes {
    // Boolean Data Type
    Boolean one = false;

    // Byte Data Type
    byte a = 10;
    byte b = -20;

    // Short Data Type
    short s = 10000;
    short r = -5000;

    // Int Data Type
    int x = 100000;
    int y = -200000;

    // Long Data Type
    long p = 100000L;
    long q = -200000L;

    // Double Data Type
    double d1 = 12.3;

    // Char Data Type
    char letterA = 'A';

}
